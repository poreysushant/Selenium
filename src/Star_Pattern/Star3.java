package Star_Pattern;

public class Star3 
{      //1 2 3 4
	//1 //* * * *
	//2 //* * * *
	//3 //* * * *
	 
	public static void main(String[]args)
	{
		//outer for loop for row
		//i=1 1<=3
		
		for(int i=1; i<=3; i++)
		{
			//inner for loop for column
			//a=1 1<=4
			
			for(int a=1; a<=4; a++)
			{
				System.out.print("*"+" ");
			}
			    System.out.println();
			    
			}
		}
	}

	


